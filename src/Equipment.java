public class Equipment {
    String equipped;
    int weaponAttackPower;
    int weaponCrit;
    int weaponAcc;

    public Equipment (String equipped, int weaponAttackPower, int weaponCrit, int weaponAcc){
        this.equipped = equipped;
        this.weaponAttackPower = weaponAttackPower;
        this.weaponAcc = weaponAcc;
        this.weaponCrit = weaponCrit;
    }
}
